public class retangulo {
    static void calcularAreaRetangulo (int largura, int altura) {
        int area = largura * altura;
        System.out.println("A área será: " + area);
    }
        
    public static void main(String[] args) {
        calcularAreaRetangulo(5,10);
        calcularAreaRetangulo(5, 2);
        calcularAreaRetangulo(12, 24);
    
        
    }
    
}

